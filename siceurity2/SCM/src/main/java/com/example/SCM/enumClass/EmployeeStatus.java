package com.example.SCM.enumClass;

public enum EmployeeStatus {
    ACTIVE,        // বর্তমানে কর্মরত আছেন
    INACTIVE,      // সাময়িকভাবে নিষ্ক্রিয়
    SUSPENDED,     // সাময়িক বরখাস্ত বা সাসপেন্ডেড
    TERMINATED,    // স্থায়ীভাবে চাকরিচ্যুত বা ছাঁটাইকৃত
    RESIGNED,      // নিজে থেকে পদত্যাগ করেছেন
    RETIRED        // অবসরপ্রাপ্ত
}
